<?php 

// Pengkondisian denga switch case 

$warna = "Merah";

switch ($warna) {

    case 'Merah';
      echo "Berhenti";
    break;

    case 'Kuning';
      echo "hati hati";
    break;

    case 'Hijau';
      echo "Maju ke depan";
    break;

    default;
      echo "Lampu sudah rusak";
    break;
}









?>